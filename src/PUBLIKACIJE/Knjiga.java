package PUBLIKACIJE;

public class Knjiga {

	private int idKnjige;
	private String naslovKnjige;
	private String ISBN;
	private int godinaIzdanja;
	private String jezik;
	private int tiraz;
	private int idAutora;
	private int idIzdavaca;
	public int getIdKnjige() {
		return idKnjige;
	}
	public void setIdKnjige(int idKnjige) {
		this.idKnjige = idKnjige;
	}
	public String getNaslovKnjige() {
		return naslovKnjige;
	}
	public void setNaslovKnjige(String naslovKnjige) {
		this.naslovKnjige = naslovKnjige;
	}
	public String getISBN() {
		return ISBN;
	}
	public void setISBN(String iSBN) {
		ISBN = iSBN;
	}
	public int getGodinaIzdanja() {
		return godinaIzdanja;
	}
	public void setGodinaIzdanja(int godinaIzdanja) {
		this.godinaIzdanja = godinaIzdanja;
	}
	public String getJezik() {
		return jezik;
	}
	public void setJezik(String jezik) {
		this.jezik = jezik;
	}
	public int getTiraz() {
		return tiraz;
	}
	public void setTiraz(int tiraz) {
		this.tiraz = tiraz;
	}
	public int getIdAutora() {
		return idAutora;
	}
	public void setIdAutora(int idAutora) {
		this.idAutora = idAutora;
	}
	public int getIdIzdavaca() {
		return idIzdavaca;
	}
	public void setIdIzdavaca(int idIzdavaca) {
		this.idIzdavaca = idIzdavaca;
	}
	
	
	
}
